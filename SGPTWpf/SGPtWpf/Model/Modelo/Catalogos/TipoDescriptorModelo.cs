        public static bool Insert(TipoDescriptorModelo modelo, Boolean insertar)
        {
            bool result = false;
            if (!(modelo == null))
            {
                try
                {
                    using (_context = new SGPTEntidades())
                    {
                        if (!(sincronizar))
                        {
                            var valorMantenimiento = _context.Database.ExecuteSqlCommand(
                            "SELECT pg_catalog.setval(pg_get_serial_sequence('sgpt.tiposdescriptores', 'idtd'), (SELECT MAX(idtd) FROM sgpt.tiposdescriptores) + 1);");
                            sincronizar = true;
                        }                        //var items = from c in _context.maestrocatalogos select c.idmaestrocatalogo;
                        var tablaDestino = new tiposdescriptore
                        {
                            //idtd = modelo.id,
                            tipotd = modelo.descripcion,
                            sistematd = false,
                            estadotd = "A"
                        };
                        _context.tiposdescriptores.Add(tablaDestino);
                        _context.SaveChanges();
                        modelo.id = tablaDestino.idtd;
                        modelo.sistema = tablaDestino.sistematd;
                        modelo.estado = tablaDestino.estadotd;
                        result = true;
                    }
                }
                catch (Exception e)
                {
                    //Captura de fallo en la insercion
                    if (e.Source != null)
                        MessageBox.Show("Exception en insertar : {0}", e.Source);
                    throw;
                }
                return result;
            }
            else
            {
                return result;
            }
        }

