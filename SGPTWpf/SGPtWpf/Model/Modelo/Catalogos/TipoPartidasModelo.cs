        public static bool Insert(TipoPartidaModelo modelo, Boolean insertar)
        {
            bool result = false;
            if (!(modelo == null))
            {
                try
                {
                    using (_context = new SGPTEntidades())
                    {
                        if (!(sincronizar))
                        {
                            var valorMantenimiento = _context.Database.ExecuteSqlCommand(
                            "SELECT pg_catalog.setval(pg_get_serial_sequence('sgpt.tipopartidas', 'idtp'), (SELECT MAX(idtp) FROM sgpt.tipopartidas) + 1);");
                            sincronizar = true;
                        }                        //var items = from c in _context.maestrocatalogos select c.idmaestrocatalogo;
                        var tablaDestino = new tipopartida
                        {
                            //idtp = modelo.id,
                            descripciontp = modelo.descripcion,
                            sistematp = false,
                            estadotp = "A"
                        };
                        _context.tipopartidas.Add(tablaDestino);
                        _context.SaveChanges();
                        modelo.id = tablaDestino.idtp;
                        modelo.sistema = tablaDestino.sistematp;
                        modelo.estado = tablaDestino.estadotp;
                        result = true;
                    }
                }
                catch (Exception e)
                {
                    //Captura de fallo en la insercion
                    if (e.Source != null)
                        MessageBox.Show("Exception en insertar : {0}", e.Source);
                    throw;
                }
                return result;
            }
            else
            {
                return result;
            }
        }

