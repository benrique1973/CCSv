        public static bool Insert(CorrespondenciaTipoModelo modelo, Boolean insertar)
        {
            bool result = false;
            if (!(modelo == null))
            {
                try
                {
                    using (_context = new SGPTEntidades())
                    {
                        if (!(sincronizar))
                        {
                            var valorMantenimiento = _context.Database.ExecuteSqlCommand(
                            "SELECT pg_catalog.setval(pg_get_serial_sequence('sgpt.correspondenciatipos', 'idct'), (SELECT MAX(idct) FROM sgpt.correspondenciatipos) + 1);");
                            sincronizar = true;
                        }
                        //var items = from c in _context.maestrocatalogos select c.idmaestrocatalogo;
                        var tablaDestino = new CapaDatos.correspondenciatipos
                        {
                            //idct = modelo.id,
                            descripcionct = modelo.descripcion,
                            sistemact = false,
                            estadoct = "A"
                        };
                        _context.correspondenciatipos.Add(tablaDestino);
                        _context.SaveChanges();
                        modelo.id = tablaDestino.idct;
                        modelo.sistema = tablaDestino.sistemact;
                        modelo.estado = tablaDestino.estadoct;
                        result = true;
                    }
                }
                catch (Exception e)
                {
                    //Captura de fallo en la insercion
                    if (e.Source != null)
                        MessageBox.Show("Exception en insertar :  \n"+ e);
                    throw;
                }
                return result;
            }
            else
            {
                return result;
            }
        }

